void main() {
  // Variabel untuk menyimpan nilai pinjaman (p), waktu (t), dan tingkat bunga (r)
  double p = 1000; // Misalnya, pinjaman sebesar $1000
  double t = 2; // Misalnya, pinjaman berlangsung selama 2 tahun
  double r = 5; // Misalnya, tingkat bunga 5% per tahun

  // Hitung bunga sederhana menggunakan rumus yang diberikan
  double bunga = (p * t * r) / 100;

  // Cetak hasilnya
  print('Bunga sederhana yang diperoleh adalah: $bunga');
}
